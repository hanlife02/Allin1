type LocaleShape<T> = T extends string
  ? string
  : T extends readonly (infer U)[]
    ? readonly LocaleShape<U>[]
    : T extends object
      ? { [K in keyof T]: LocaleShape<T[K]> }
      : T

export type LocaleDict = LocaleShape<typeof zh>
